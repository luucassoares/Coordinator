import Foundation
import UIKit
class Colors {
    //GRADIENT
    static let GOLD_GRADIENT_LEFT = UIColorFromRGB(rgbValue: 0xB3873E)
    static let GOLD_GRADIENT_RIGHT = UIColorFromRGB(rgbValue: 0xB69958)
    static let BLUE_GRADIENT_LEFT = UIColorFromRGB(rgbValue: 0x142534)
    static let BLUE_GRADIENT_RIGHT = UIColorFromRGB(rgbValue: 0x173855)
    
    static func UIColorFromRGB(rgbValue: UInt) -> UIColor {
        return UIColor(
            red: CGFloat((rgbValue & 0xFF0000) >> 16) / 255.0,
            green: CGFloat((rgbValue & 0x00FF00) >> 8) / 255.0,
            blue: CGFloat(rgbValue & 0x0000FF) / 255.0,
            alpha: CGFloat(1.0)
        )
    }
    
    static func UIColorFromRGBWithAlpha(rgbValue: UInt, alpha: UInt) -> UIColor {
        return UIColor(
            red: CGFloat((rgbValue & 0xFF0000) >> 16) / 255.0,
            green: CGFloat((rgbValue & 0x00FF00) >> 8) / 255.0,
            blue: CGFloat(rgbValue & 0x0000FF) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
}
extension UIColor {
    
    convenience init(hex:Int, alpha:CGFloat = 1.0) {
        self.init(
            red:   CGFloat((hex & 0xFF0000) >> 16) / 255.0,
            green: CGFloat((hex & 0x00FF00) >> 8)  / 255.0,
            blue:  CGFloat((hex & 0x0000FF) >> 0)  / 255.0,
            alpha: alpha
        )
    }
    
}
